package sysimg;

public class InputOverflowBinException extends BinException {

	private static final long serialVersionUID = 1L;

	public InputOverflowBinException() {
		super();
	}

	public InputOverflowBinException(String message, Throwable cause) {
		super(message, cause);
	}

	public InputOverflowBinException(String message) {
		super(message);
	}

	public InputOverflowBinException(Throwable cause) {
		super(cause);
	}

}

