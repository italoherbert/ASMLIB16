public class OutputFIBinException extends BinException {

	private static final long serialVersionUID = 1L;

	public OutputFIBinException() {
		super();
	}

	public OutputFIBinException(String message, Throwable cause) {
		super(message, cause);
	}

	public OutputFIBinException(String message) {
		super(message);
	}

	public OutputFIBinException(Throwable cause) {
		super(cause);
	}

}
